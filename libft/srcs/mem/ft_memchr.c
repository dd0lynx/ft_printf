#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = (char *)s;
	i = 0;
	while (i < n)
	{
		if (*p == (char)c)
		{
			return (p);
		}
		p++;
		i++;
	}
	return (NULL);
}
