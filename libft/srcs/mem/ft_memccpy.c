#include <string.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	while (i < n)
	{
		d[i] = (s[i]);
		if ((unsigned char)c == (s[i]))
		{
			return (dst + i + 1);
		}
		i++;
	}
	return (NULL);
}
