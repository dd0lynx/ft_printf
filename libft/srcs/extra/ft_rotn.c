#include <string.h>

char	*ft_rotn(char *str, int n)
{
	int		rot;
	char	*s;

	if (!str)
		return (NULL);
	rot = n % 26;
	s = str;
	while (*s)
	{
		if (*s >= 'A' && *s <= 'Z')
			((*s + rot) <= 'Z') ? (*s += rot) : (*s = *s + rot - 26);
		else if (*s >= 'a' && *s <= 'z')
			((*s + rot) <= 'z') ? (*s += rot) : (*s = *s + rot - 26);
		s++;
	}
	return (str);
}
