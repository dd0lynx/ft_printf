char	*ft_strcat(char *dst, char *src)
{
	char	*p;

	p = dst;
	while (*p != '\0')
		p++;
	while (*src != '\0')
	{
		*p = *src;
		p++;
		src++;
	}
	*p = '\0';
	return (dst);
}
